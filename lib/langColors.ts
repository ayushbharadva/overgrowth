export function languageColor(lang: string): string {
  const known = LINGUIST[lang];
  if (known) return known;
  // deterministic pastel fallback
  const [h] = hashString(lang);
  const hue = h % 360;
  return `hsl(${hue}, 55%, 62%)`;
}
